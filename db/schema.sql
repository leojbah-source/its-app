-- =============================================================================
-- KCA INDIAN TALENT SCAN (ITS) -- POSTGRESQL SCHEMA
-- Kerala Catholic Association, Bahrain  |  talentscan.kcabah.com
-- =============================================================================
-- Target: PostgreSQL 14+ (uses GENERATED ALWAYS AS ... STORED columns)
-- Run on a fresh database/schema. To rebuild from scratch:
--   DROP SCHEMA public CASCADE; CREATE SCHEMA public;
--
-- DESIGN-RULE TRACEABILITY
-- Every constraint/trigger below is commented with the rule number(s) from the
-- Master Context it enforces, e.g. "rule #3". Rule list for reference:
--   1  All annual parameters live in year_config -- nothing hard-coded per year
--   2  PDFs include KCA logo + sponsor logo from year_config
--   3  Chest numbers assigned randomly on the day, post-attendance; time-slot
--      mode = lot draw per slot, continuous numbering across slots
--   4  Manual chest number entry restricted to Chairman/SuperAdmin
--   5  Judges see chest numbers only, never participant names
--   6  Live ranking visible only to the scoring judge as they enter scores
--   7  Divergence threshold = % of total participants (year_config-driven)
--   8  Exact tie -> Chairman-unlocked tiebreaker marks (1-10/judge), logged
--   9  Judge refusal -> mandatory statement -> judge_flags -> Chairman notified
--   10 Judge blacklist warns admin on assignment, tracked annually
--   11 Judge contact info restricted to SuperAdmin + Chairman
--   12 OTP sent manually by Convener/Admin at briefing, not automatically
--   13 Two-stage result publication: Finalise+Print -> Chairman publish to PWA
--   14 Extra/consolation 4th-place prize: Chairman only, before Stage 2, no rank pts
--   15 Group Championship = one award per age group, gender-agnostic
--   16 School awards = SUM(rank_points + grade_points + participation_bonus_pts)
--   17 Teacher name 'NOT_APPLICABLE' option, excluded from teacher awards
--   18 Teacher name deadline configurable, can be updated post-contest
--   19 Team attendance confirmed per member; substitutions need Admin approval
--   20 Cancelled event -> one swap window after initial list publication
--   21 PWA login = first 4 chars of name + last 4 digits of CPR
--   22 Chest numbers never shown in PWA
--   23 PWA "My Results" = per-event grade/rank pts/grade pts + running totals
--   24 Awards screen restricted to Chairman role
--   25 audit_log is INSERT-ONLY
--   26 Auto Schedule Draft generated by scheduler.js from Admin inputs
-- =============================================================================

CREATE EXTENSION IF NOT EXISTS citext;

-- =============================================================================
-- SECTION 0: ENUM TYPES
-- =============================================================================

CREATE TYPE user_role AS ENUM ('SuperAdmin','Admin','Coordinator','Chairman','Judge','Viewer');
CREATE TYPE event_kind AS ENUM ('individual','team');
CREATE TYPE registration_status AS ENUM ('registered','attended','absent','withdrawn','swapped');

-- =============================================================================
-- SECTION 1: SETUP / YEAR CONFIGURATION
-- =============================================================================

CREATE TABLE year_config (
    id                          SERIAL PRIMARY KEY,
    year                        INT NOT NULL UNIQUE,                 -- calendar year, e.g. 2026
    event_year_label            TEXT NOT NULL,                       -- e.g. 'KCA Indian Talent Scan 2026'
    is_active                   BOOL NOT NULL DEFAULT FALSE,         -- the single "current" year app operates on
    kca_logo_url                TEXT,
    sponsor_logo_url            TEXT,
    sponsor_name                TEXT,
    kca_iban                    TEXT,
    benefit_pay_number          TEXT,
    max_individual_events       INT NOT NULL DEFAULT 12,
    category_cap                INT NOT NULL DEFAULT 6,
    kca_special_min_points      INT NOT NULL DEFAULT 30,
    min_entries_threshold       INT NOT NULL DEFAULT 5,
    split_threshold             INT NOT NULL DEFAULT 25,
    no_prize_below              INT NOT NULL DEFAULT 3,
    rank_pts_first              NUMERIC(6,2) NOT NULL DEFAULT 5,
    rank_pts_second             NUMERIC(6,2) NOT NULL DEFAULT 3,
    rank_pts_third              NUMERIC(6,2) NOT NULL DEFAULT 1,
    participation_bonus_pts     NUMERIC(6,2) NOT NULL DEFAULT 0.5,
    grade_a_pct                 NUMERIC(5,2) NOT NULL DEFAULT 70,
    grade_b_pct                 NUMERIC(5,2) NOT NULL DEFAULT 50,
    grade_a_pts                 NUMERIC(6,2) NOT NULL DEFAULT 3,
    grade_b_pts                 NUMERIC(6,2) NOT NULL DEFAULT 2,
    grade_c_pts                 NUMERIC(6,2) NOT NULL DEFAULT 1,
    divergence_threshold_pct    NUMERIC(5,2) NOT NULL DEFAULT 20,    -- rule #7
    reg_deadline                TIMESTAMPTZ,
    team_reg_deadline           TIMESTAMPTZ,
    teacher_name_deadline       TIMESTAMPTZ,                        -- rule #18
    result_template_url         TEXT,
    photo_crop_width            INT NOT NULL DEFAULT 413,
    photo_crop_height           INT NOT NULL DEFAULT 531,
    initial_list_published      BOOL NOT NULL DEFAULT FALSE,         -- gates the rule #20 swap window
    initial_list_published_at   TIMESTAMPTZ,
    created_at                  TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at                  TIMESTAMPTZ NOT NULL DEFAULT now(),
    CHECK (grade_a_pct BETWEEN 0 AND 100),
    CHECK (grade_b_pct BETWEEN 0 AND 100),
    CHECK (grade_a_pct > grade_b_pct),
    CHECK (divergence_threshold_pct BETWEEN 0 AND 100)
);
COMMENT ON TABLE year_config IS 'One row per competition year. ALL annual/tunable parameters live here (rule #1) -- nothing year-specific should ever be hard-coded in application code. Every other table that varies by year hangs off year_id.';
COMMENT ON COLUMN year_config.kca_logo_url IS 'Used on every generated PDF together with sponsor_logo_url (rule #2).';
COMMENT ON COLUMN year_config.divergence_threshold_pct IS 'Absolute divergence threshold per event = ROUND(total_participants * pct / 100) -- see fn_divergence_abs_threshold() (rule #7).';

-- Only one year may be "active" at a time.
CREATE UNIQUE INDEX ux_year_config_single_active ON year_config (is_active) WHERE is_active = TRUE;

CREATE TABLE users (
    id              SERIAL PRIMARY KEY,
    full_name       TEXT NOT NULL,
    email           CITEXT UNIQUE,
    phone           TEXT,
    password_hash   TEXT NOT NULL,
    role            user_role NOT NULL,
    is_active       BOOL NOT NULL DEFAULT TRUE,
    last_login_at   TIMESTAMPTZ,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT now()
);
COMMENT ON TABLE users IS 'JWT-authenticated application users. role drives both API authorization and several DB-level checks below (the app must SET LOCAL app.current_role / app.current_user_id per request -- see SECTION 9).';

CREATE TABLE schools (
    id              SERIAL PRIMARY KEY,
    name            TEXT NOT NULL,
    short_code      TEXT UNIQUE,
    contact_person  TEXT,
    contact_phone   TEXT,
    contact_email   TEXT,
    address         TEXT,
    is_active       BOOL NOT NULL DEFAULT TRUE,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT now()
);
COMMENT ON TABLE schools IS 'Participating units/schools/parish groups -- the award unit for School Awards (rule #16) and Group Championship (rule #15).';

CREATE TABLE age_groups (
    id          SERIAL PRIMARY KEY,
    year_id     INT NOT NULL REFERENCES year_config(id) ON DELETE CASCADE,
    code        TEXT NOT NULL,           -- 'G1'..'G5'
    label       TEXT NOT NULL,           -- e.g. 'Sub Junior (5-7 yrs)'
    dob_from    DATE NOT NULL,
    dob_to      DATE NOT NULL,
    sort_order  INT NOT NULL DEFAULT 0,
    UNIQUE (year_id, code),
    CHECK (dob_from <= dob_to)
);
COMMENT ON TABLE age_groups IS 'DOB-based age groups, redefined every year (rule #1). A participant''s age_group_id is auto-derived from DOB via fn_assign_age_group().';

CREATE TABLE categories (
    id          SERIAL PRIMARY KEY,
    year_id     INT NOT NULL REFERENCES year_config(id) ON DELETE CASCADE,
    code        TEXT NOT NULL,
    name        TEXT NOT NULL,
    sort_order  INT NOT NULL DEFAULT 0,
    UNIQUE (year_id, code)
);
COMMENT ON TABLE categories IS 'The ~6 event categories, configured per year so they can be renamed/added/retired without code changes (rule #1).';

CREATE TABLE events (
    id                          SERIAL PRIMARY KEY,
    year_id                     INT NOT NULL REFERENCES year_config(id) ON DELETE CASCADE,
    category_id                 INT REFERENCES categories(id),
    event_code                  TEXT NOT NULL,
    event_name                  TEXT NOT NULL,
    event_kind                  event_kind NOT NULL DEFAULT 'individual',
    is_stage_event              BOOL NOT NULL DEFAULT TRUE,
    max_participants_per_team   INT,
    min_participants_per_team   INT,
    time_slot_mode              BOOL NOT NULL DEFAULT FALSE,    -- rule #3
    is_cancelled                BOOL NOT NULL DEFAULT FALSE,    -- rule #20
    cancelled_at                TIMESTAMPTZ,
    cancel_reason               TEXT,
    cancelled_by                INT REFERENCES users(id),
    sort_order                  INT NOT NULL DEFAULT 0,
    created_at                  TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at                  TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (year_id, event_code)
);
COMMENT ON TABLE events IS '~54 individual + 9 team events per year. time_slot_mode flags high-volume events that use lot-draw-per-slot with continuous chest numbering (rule #3). is_cancelled drives the rule #20 one-time swap window.';

CREATE INDEX idx_events_year ON events(year_id);

CREATE TABLE event_age_groups (
    event_id      INT NOT NULL REFERENCES events(id) ON DELETE CASCADE,
    age_group_id  INT NOT NULL REFERENCES age_groups(id) ON DELETE CASCADE,
    PRIMARY KEY (event_id, age_group_id)
);
COMMENT ON TABLE event_age_groups IS 'Which age groups a given event is open to.';

CREATE TABLE event_criteria (
    id               SERIAL PRIMARY KEY,
    event_id         INT NOT NULL REFERENCES events(id) ON DELETE CASCADE,
    criterion_name   TEXT NOT NULL,
    max_score        NUMERIC(5,2) NOT NULL CHECK (max_score > 0),
    sequence_order   INT NOT NULL DEFAULT 1,
    UNIQUE (event_id, sequence_order)
);
COMMENT ON TABLE event_criteria IS 'Up to 6 scoring criteria per event; the max_score values must sum to 100 -- enforced by trg_event_criteria_check.';

CREATE TABLE event_time_slots (
    id                      SERIAL PRIMARY KEY,
    event_id                INT NOT NULL REFERENCES events(id) ON DELETE CASCADE,
    sub_group               CHAR(1),                 -- batch label within the event, e.g. 'A','B','C'
    slot_label              TEXT NOT NULL,
    reporting_time          TIME,
    participant_capacity    INT NOT NULL CHECK (participant_capacity > 0),
    chest_no_start           INT,                     -- auto-computed, see fn_recalc_slot_chest_numbers()
    sort_order               INT NOT NULL DEFAULT 1,
    UNIQUE (event_id, slot_label)
);
COMMENT ON TABLE event_time_slots IS 'For time_slot_mode events: one row per lot-draw slot. chest_no_start is recalculated automatically so chest numbers stay continuous across all slots of the event (rule #3).';

-- =============================================================================
-- SECTION 2: JUDGES
-- =============================================================================

CREATE TABLE judges (
    id                SERIAL PRIMARY KEY,
    user_id           INT REFERENCES users(id),       -- optional login account
    full_name         TEXT NOT NULL,
    phone             TEXT,
    whatsapp          TEXT,
    email             TEXT,
    bio               TEXT,
    is_blacklisted    BOOL NOT NULL DEFAULT FALSE,     -- rule #10
    blacklist_reason  TEXT,
    blacklist_date    DATE,
    blacklisted_by    INT REFERENCES users(id),
    otp_code          TEXT,                            -- rule #12
    otp_sent_at       TIMESTAMPTZ,
    otp_sent_by       INT REFERENCES users(id),
    created_at        TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at        TIMESTAMPTZ NOT NULL DEFAULT now()
);
COMMENT ON TABLE judges IS 'phone/whatsapp/email are restricted fields -- the API must only expose them to SuperAdmin/Chairman (rule #11). Use the v_judges_public view for every other role. otp_code is set manually by Convener/Admin at briefing time, never automatically (rule #12).';
COMMENT ON COLUMN judges.is_blacklisted IS 'trg_judge_assignment_blacklist_warning raises a WARNING when a blacklisted judge is assigned (rule #10); the app should surface this to the assigning Admin and it feeds the annual blacklist report.';

CREATE INDEX idx_judges_is_blacklisted ON judges(is_blacklisted);

CREATE TABLE judge_flags (
    id                       SERIAL PRIMARY KEY,
    judge_id                 INT NOT NULL REFERENCES judges(id),
    event_id                 INT REFERENCES events(id),
    year_id                  INT NOT NULL REFERENCES year_config(id),
    flag_type                TEXT NOT NULL CHECK (flag_type IN ('declined_revision','bias_noted')),
    statement                TEXT NOT NULL,
    flagged_at               TIMESTAMPTZ NOT NULL DEFAULT now(),
    reviewed_by_chairman     BOOL NOT NULL DEFAULT FALSE
);
COMMENT ON TABLE judge_flags IS 'Created when a judge refuses a divergence/tie review (rule #9). statement is mandatory; Chairman is notified and reviews via reviewed_by_chairman.';

CREATE INDEX idx_judge_flags_judge_year ON judge_flags(judge_id, year_id);

CREATE TABLE judge_assignments (
    id              SERIAL PRIMARY KEY,
    judge_id        INT NOT NULL REFERENCES judges(id),
    event_id        INT NOT NULL REFERENCES events(id),
    time_slot_id    INT REFERENCES event_time_slots(id),
    assigned_by     INT REFERENCES users(id),
    assigned_at     TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (judge_id, event_id, time_slot_id)
);
COMMENT ON TABLE judge_assignments IS 'Exactly 3 judges expected per event (enforced at application level / report, not a hard DB constraint to allow temporary under/over-assignment while staffing).';

-- =============================================================================
-- SECTION 3: PARTICIPANTS & MEMBERSHIP
-- =============================================================================

CREATE TABLE participants (
    id                  SERIAL PRIMARY KEY,
    year_id             INT NOT NULL REFERENCES year_config(id),
    school_id           INT REFERENCES schools(id),
    cpr_number          TEXT NOT NULL,
    full_name           TEXT NOT NULL,
    dob                 DATE NOT NULL,
    gender              CHAR(1) CHECK (gender IN ('M','F')),
    age_group_id        INT REFERENCES age_groups(id),       -- auto-derived, see fn_assign_age_group()
    guardian_name       TEXT,
    guardian_phone      TEXT,
    photo_url           TEXT,
    cpr_scan_url        TEXT,
    membership_status   TEXT,
    pwa_username        TEXT,                                -- auto-derived, see fn_generate_pwa_username()
    created_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (year_id, cpr_number)
);
COMMENT ON TABLE participants IS 'One row per participant per year. age_group_id and pwa_username are computed automatically by triggers -- never set them directly from the app.';
COMMENT ON COLUMN participants.pwa_username IS 'PWA login identifier = first 4 chars of full_name + last 4 digits of cpr_number (rule #21), lower-cased. Chest numbers are NEVER exposed via the PWA (rule #22) -- this column has nothing to do with chest numbers.';

CREATE INDEX idx_participants_year_cpr ON participants(year_id, cpr_number);

CREATE TABLE membership_verifications (
    id               SERIAL PRIMARY KEY,
    participant_id   INT REFERENCES participants(id),
    cpr_number       TEXT NOT NULL,
    member_status    TEXT,
    family_id        TEXT,
    raw_response     JSONB,
    checked_at       TIMESTAMPTZ NOT NULL DEFAULT now(),
    checked_by       INT REFERENCES users(id)
);
COMMENT ON TABLE membership_verifications IS 'Cache/audit trail of lookups against the live KCA membership API (mem.kcabah.com).';

-- =============================================================================
-- SECTION 4: TEAMS & TEAM MEMBERS
-- =============================================================================

CREATE TABLE teams (
    id            SERIAL PRIMARY KEY,
    year_id       INT NOT NULL REFERENCES year_config(id),
    event_id      INT NOT NULL REFERENCES events(id),
    school_id     INT REFERENCES schools(id),
    age_group_id  INT REFERENCES age_groups(id),
    team_name     TEXT NOT NULL,
    created_at    TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (event_id, team_name)
);
COMMENT ON TABLE teams IS 'One row per team entered into one of the 9 team events.';

CREATE TABLE team_members (
    id                       SERIAL PRIMARY KEY,
    team_id                  INT NOT NULL REFERENCES teams(id) ON DELETE CASCADE,
    participant_id           INT NOT NULL REFERENCES participants(id),
    is_substitute            BOOL NOT NULL DEFAULT FALSE,
    substitute_reason        TEXT,
    approved_by              INT REFERENCES users(id),
    approved_at              TIMESTAMPTZ,
    attendance_confirmed     BOOL NOT NULL DEFAULT FALSE,    -- rule #19: each member confirmed individually
    confirmed_by             INT REFERENCES users(id),
    confirmed_at             TIMESTAMPTZ,
    created_at               TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (team_id, participant_id),
    CHECK (NOT is_substitute OR approved_by IS NOT NULL)      -- rule #19: substitutions require Admin approval
);
COMMENT ON TABLE team_members IS 'Per rule #19: every member''s attendance is confirmed individually (attendance_confirmed/confirmed_by/confirmed_at) and any substitution requires an approving Admin (approved_by) -- the CHECK constraint makes this non-negotiable at the DB level. All changes are additionally captured in audit_log.';

CREATE INDEX idx_team_members_team ON team_members(team_id);

-- =============================================================================
-- SECTION 5: REGISTRATIONS
-- =============================================================================

CREATE TABLE registrations (
    id                       SERIAL PRIMARY KEY,
    year_id                  INT NOT NULL REFERENCES year_config(id),
    participant_id           INT REFERENCES participants(id),
    team_id                  INT REFERENCES teams(id),
    event_id                 INT NOT NULL REFERENCES events(id),
    age_group_id             INT NOT NULL REFERENCES age_groups(id),
    category_id              INT REFERENCES categories(id),
    time_slot_id             INT REFERENCES event_time_slots(id),
    status                   registration_status NOT NULL DEFAULT 'registered',
    dance_teacher             TEXT,   -- NULL, 'NOT_APPLICABLE', or teacher name (rule #17)
    music_teacher              TEXT,   -- NULL, 'NOT_APPLICABLE', or teacher name (rule #17)
    attendance_marked_by      INT REFERENCES users(id),
    attendance_marked_at      TIMESTAMPTZ,
    registered_at             TIMESTAMPTZ NOT NULL DEFAULT now(),
    registered_by             INT REFERENCES users(id),
    updated_at                TIMESTAMPTZ NOT NULL DEFAULT now(),
    CHECK ((participant_id IS NOT NULL AND team_id IS NULL) OR (participant_id IS NULL AND team_id IS NOT NULL))
);
COMMENT ON TABLE registrations IS 'One row per individual-event entry (participant_id set) or per team-event entry (team_id set). dance_teacher/music_teacher accept the literal value ''NOT_APPLICABLE'' for self-taught participants, who are then excluded from teacher awards by application logic (rule #17); the editable window is governed by year_config.teacher_name_deadline (rule #18), which the app may relax even after contests conclude.';

CREATE UNIQUE INDEX ux_registrations_participant_event ON registrations(participant_id, event_id) WHERE participant_id IS NOT NULL;
CREATE UNIQUE INDEX ux_registrations_team_event ON registrations(team_id, event_id) WHERE team_id IS NOT NULL;
CREATE INDEX idx_registrations_event_participant ON registrations(event_id, participant_id);

CREATE TABLE event_swap_requests (
    id                  SERIAL PRIMARY KEY,
    registration_id     INT NOT NULL UNIQUE REFERENCES registrations(id),  -- UNIQUE => exactly one swap window (rule #20)
    original_event_id   INT NOT NULL REFERENCES events(id),
    new_event_id        INT REFERENCES events(id),
    reason               TEXT,
    status                TEXT NOT NULL DEFAULT 'pending' CHECK (status IN ('pending','approved','rejected')),
    requested_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    requested_by          INT REFERENCES users(id),
    approved_by           INT REFERENCES users(id),
    approved_at           TIMESTAMPTZ
);
COMMENT ON TABLE event_swap_requests IS 'Rule #20: if an event is cancelled after the initial list is published, affected participants get exactly ONE swap. The UNIQUE constraint on registration_id enforces "one window" at the database level.';

-- =============================================================================
-- SECTION 6: CHEST NUMBERS
-- =============================================================================

CREATE TABLE chest_assignments (
    id                SERIAL PRIMARY KEY,
    year_id           INT NOT NULL REFERENCES year_config(id),
    event_id          INT NOT NULL REFERENCES events(id),
    time_slot_id      INT REFERENCES event_time_slots(id),
    registration_id   INT NOT NULL UNIQUE REFERENCES registrations(id),
    chest_number      INT NOT NULL CHECK (chest_number > 0),
    allocation_mode   TEXT NOT NULL CHECK (allocation_mode IN ('auto','timeslot','manual')),
    allocated_by      INT NOT NULL REFERENCES users(id),
    allocated_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (event_id, chest_number)
);
COMMENT ON TABLE chest_assignments IS 'Chest numbers are assigned the day of the event, after attendance is marked -- never in advance (rule #3). allocation_mode = manual is restricted to Chairman/SuperAdmin and enforced by trg_chest_manual_role_check (rule #4).';

-- =============================================================================
-- SECTION 7: SCORING, TIES & RESULTS
-- =============================================================================

CREATE TABLE scores (
    id                     BIGSERIAL PRIMARY KEY,
    judge_assignment_id    INT NOT NULL REFERENCES judge_assignments(id),
    registration_id        INT NOT NULL REFERENCES registrations(id),
    criterion_id           INT NOT NULL REFERENCES event_criteria(id),
    score_value            NUMERIC(5,2) NOT NULL CHECK (score_value >= 0),
    entered_at             TIMESTAMPTZ NOT NULL DEFAULT now(),
    entered_by             INT REFERENCES users(id),
    updated_at             TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (judge_assignment_id, registration_id, criterion_id)
);
COMMENT ON TABLE scores IS 'One row per judge x participant x criterion. trg_scores_check_max guarantees score_value never exceeds the criterion''s max_score. The "live ranking visible only to the scoring judge" rule (#6) and "judges see chest numbers only" rule (#5) are enforced at the API layer via v_judge_scoring_board -- judges'' read access should never touch participants or registrations directly.';

CREATE TABLE tiebreaker_unlocks (
    id            SERIAL PRIMARY KEY,
    event_id      INT NOT NULL REFERENCES events(id),
    sub_group     CHAR(1),
    unlocked_by   INT NOT NULL REFERENCES users(id),
    unlocked_at   TIMESTAMPTZ NOT NULL DEFAULT now(),
    locked_at     TIMESTAMPTZ
);
COMMENT ON TABLE tiebreaker_unlocks IS 'A Chairman must explicitly unlock a tiebreaker session for an event/sub_group before an Admin can key in marks (rule #8). trg_tiebreaker_marks_check_unlock verifies unlocked_by holds the Chairman role and that the session is not yet locked.';

CREATE TABLE tiebreaker_marks (
    id                        SERIAL PRIMARY KEY,
    event_id                  INT NOT NULL REFERENCES events(id),
    sub_group                 CHAR(1),
    participant_reg_id        INT NOT NULL REFERENCES registrations(id),
    judge_id                  INT NOT NULL REFERENCES judges(id),
    mark                      INT NOT NULL CHECK (mark BETWEEN 1 AND 10),
    unlock_id                 INT NOT NULL REFERENCES tiebreaker_unlocks(id),
    entered_by                INT NOT NULL REFERENCES users(id),
    approved_by_chairman      INT REFERENCES users(id),
    entered_at                TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (participant_reg_id, judge_id, unlock_id)
);
COMMENT ON TABLE tiebreaker_marks IS 'Exact-tie process (rule #8): each judge gives a 1-10 mark per tied participant; Admin keys it in under Chairman supervision; everything is captured in audit_log via trg_audit_tiebreaker_marks.';

CREATE TABLE event_results (
    id                          SERIAL PRIMARY KEY,
    registration_id             INT NOT NULL UNIQUE REFERENCES registrations(id),
    event_id                    INT NOT NULL REFERENCES events(id),
    prize_place                 INT CHECK (prize_place > 0),
    grade                       CHAR(1) CHECK (grade IN ('A','B','C')),
    extra_prize_type            TEXT CHECK (extra_prize_type IS NULL OR extra_prize_type IN ('additional_3rd','consolation')),
    extra_prize_approved_by     INT REFERENCES users(id),
    rank_points                 NUMERIC(6,2) NOT NULL DEFAULT 0,   -- 0 for any extra/consolation prize (rule #14)
    grade_points                NUMERIC(6,2) NOT NULL DEFAULT 0,
    participation_bonus_pts     NUMERIC(6,2) NOT NULL DEFAULT 0,
    total_points                NUMERIC(6,2) GENERATED ALWAYS AS (rank_points + grade_points + participation_bonus_pts) STORED,
    tie_flag                    BOOL NOT NULL DEFAULT FALSE,
    divergence_flag              BOOL NOT NULL DEFAULT FALSE,
    divergence_notes             TEXT,
    is_finalised                 BOOL NOT NULL DEFAULT FALSE,        -- Stage 1 (rule #13)
    finalised_by                  INT REFERENCES users(id),
    finalised_at                  TIMESTAMPTZ,
    is_published                  BOOL NOT NULL DEFAULT FALSE,        -- Stage 2 (rule #13)
    published_by                  INT REFERENCES users(id),
    published_at                  TIMESTAMPTZ,
    created_at                    TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at                    TIMESTAMPTZ NOT NULL DEFAULT now(),
    CHECK (NOT is_published OR is_finalised),                          -- can't publish before finalising (rule #13)
    CHECK (extra_prize_type IS NULL OR extra_prize_approved_by IS NOT NULL)
);
COMMENT ON TABLE event_results IS 'total_points (= rank_points + grade_points + participation_bonus_pts) is a generated column, guaranteeing the school-award formula (rule #16) is always consistent. The CHECK on is_published/is_finalised enforces the two-stage publication workflow (rule #13): Finalise+Print for signatures, then Chairman publishes to the PWA.';
COMMENT ON COLUMN event_results.extra_prize_type IS 'Chairman-only "4th place" extra/consolation prize, addable only before Stage 2 publication (rule #14) -- enforced by trg_event_results_extra_prize_window. Carries no rank_points.';

CREATE INDEX idx_event_results_registration ON event_results(registration_id);

-- =============================================================================
-- SECTION 8: SCHEDULING & NOTIFICATIONS
