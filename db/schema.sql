-- =============================================================================
-- SECTION 14: BLUEPRINT V4 EXTENSIONS -- TIMER MODULE
-- =============================================================================
-- Adds a Timer role + per-event time limits, a timing log with auto-computed
-- overage/DQ flags, score voiding, and result disqualification/reversal.
-- Placed BEFORE the grants section so the blanket GRANT in section 15 picks
-- up the two new tables automatically.
--
-- Three deliberate deviations from the literal instruction, called out here
-- so they're not mistaken for mistakes:
--   1. users.role is an ENUM (user_role) in this schema, not a CHECK
--      constraint -- there is no users_role_check to drop. The new value is
--      added via ALTER TYPE ... ADD VALUE, and is spelled 'Timer' (PascalCase)
--      to match the existing SuperAdmin/Admin/Coordinator/Chairman/Judge/
--      Viewer convention. Use 'Timer' exactly in JWT role claims/app code.
--   2. timer_assignments.year_id now references year_config(id) -- this
--      schema's year table is year_config, not years.
--   3. participant_timings: allotted_time_s/grace_period_s are declared
--      NOT NULL (per the original draft) but are auto-backfilled from
--      events.allotted_time_seconds/grace_period_seconds by a BEFORE INSERT
--      trigger when omitted, which is what actually delivers the "immutable
--      snapshot" behaviour the original inline comment described.
-- =============================================================================

-- -----------------------------------------------------------------------
-- 14.1 New role: Timer
-- ALTER TYPE ... ADD VALUE must run outside an explicit transaction block
-- in PostgreSQL < 12; on 12+ it may run inside one but the new value cannot
-- be used by other statements in that same transaction.
-- -----------------------------------------------------------------------
ALTER TYPE user_role ADD VALUE IF NOT EXISTS 'Timer';

-- -----------------------------------------------------------------------
-- 14.2 New columns on events: per-event timing configuration
-- -----------------------------------------------------------------------
ALTER TABLE events ADD COLUMN IF NOT EXISTS allotted_time_seconds INT NOT NULL DEFAULT 600;
ALTER TABLE events ADD COLUMN IF NOT EXISTS grace_period_seconds  INT NOT NULL DEFAULT 30;
ALTER TABLE events ADD COLUMN IF NOT EXISTS yellow_alert_seconds  INT NOT NULL DEFAULT 60;
COMMENT ON COLUMN events.allotted_time_seconds IS 'Per-participant time budget for timed events, e.g. 600 = 10 minutes.';
COMMENT ON COLUMN events.grace_period_seconds IS 'Extra time allowed past allotted_time_seconds before a DQ flag is raised.';
COMMENT ON COLUMN events.yellow_alert_seconds IS 'Warning trigger window before time runs out, e.g. 60 = 1 minute before end.';

-- -----------------------------------------------------------------------
-- 14.3 New table: participant_timings
-- -----------------------------------------------------------------------
CREATE TABLE participant_timings (
    id                    SERIAL PRIMARY KEY,
    registration_id       INT NOT NULL REFERENCES registrations(id),
    event_id              INT NOT NULL REFERENCES events(id),
    sub_group             CHAR(1),
    chest_no              INT,
    start_time            TIMESTAMPTZ,
    end_time              TIMESTAMPTZ,
    allotted_time_s       INT NOT NULL,    -- denormalised snapshot of events.allotted_time_seconds (immutability) -- see fn_fill_participant_timing_defaults()
    grace_period_s        INT NOT NULL,    -- denormalised snapshot of events.grace_period_seconds (immutability)
    time_taken_seconds    INT GENERATED ALWAYS AS
        (EXTRACT(EPOCH FROM (end_time - start_time))::INT) STORED,
    exceeded_by_seconds   INT GENERATED ALWAYS AS
        (EXTRACT(EPOCH FROM (end_time - start_time))::INT - allotted_time_s) STORED,
    within_grace          BOOL GENERATED ALWAYS AS
        (EXTRACT(EPOCH FROM (end_time - start_time))::INT > allotted_time_s
         AND EXTRACT(EPOCH FROM (end_time - start_time))::INT <= allotted_time_s + grace_period_s) STORED,
    flag_for_dq           BOOL GENERATED ALWAYS AS
        (EXTRACT(EPOCH FROM (end_time - start_time))::INT > allotted_time_s + grace_period_s) STORED,
    timed_by              INT REFERENCES users(id),
    notes                 TEXT,
    created_at            TIMESTAMPTZ NOT NULL DEFAULT now(),
    CHECK (end_time IS NULL OR start_time IS NULL OR end_time >= start_time)
);
COMMENT ON TABLE participant_timings IS 'One row per timed performance. time_taken_seconds/exceeded_by_seconds/within_grace/flag_for_dq are all generated columns computed from start_time/end_time/allotted_time_s/grace_period_s -- never set them directly.';
COMMENT ON COLUMN participant_timings.flag_for_dq IS 'TRUE once overage exceeds the grace period -- surface this to event_results.is_disqualified via application workflow, it is not auto-applied.';

CREATE INDEX idx_timings_event ON participant_timings(event_id);
CREATE INDEX idx_timings_dq ON participant_timings(flag_for_dq) WHERE flag_for_dq = TRUE;

