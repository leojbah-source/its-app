CREATE OR REPLACE FUNCTION fn_fill_participant_timing_defaults() RETURNS TRIGGER AS $$
BEGIN
    IF NEW.allotted_time_s IS NULL OR NEW.grace_period_s IS NULL THEN
        SELECT
            COALESCE(NEW.allotted_time_s, e.allotted_time_seconds),
            COALESCE(NEW.grace_period_s, e.grace_period_seconds)
        INTO NEW.allotted_time_s, NEW.grace_period_s
        FROM events e WHERE e.id = NEW.event_id;
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

