CREATE TABLE schedule (
    id                          SERIAL PRIMARY KEY,
    year_id                      INT NOT NULL REFERENCES year_config(id),
    event_id                     INT NOT NULL REFERENCES events(id),
    time_slot_id                 INT REFERENCES event_time_slots(id),
    judge_assignment_id          INT REFERENCES judge_assignments(id),
    event_date                   DATE NOT NULL,
    start_time                   TIME,
    end_time                     TIME,
    venue                        TEXT,
    status                       TEXT NOT NULL DEFAULT 'draft' CHECK (status IN ('draft','confirmed','completed')),
    generated_by_scheduler       BOOL NOT NULL DEFAULT TRUE,
    created_at                   TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at                   TIMESTAMPTZ NOT NULL DEFAULT now()
);
COMMENT ON TABLE schedule IS 'Populated by scheduler.js as a conflict-free, judge-efficient draft from Admin inputs (rule #26); status moves draft -> confirmed -> completed.';

CREATE INDEX idx_schedule_year_date ON schedule(year_id, event_date);

CREATE TABLE notifications_log (
    id                       BIGSERIAL PRIMARY KEY,
    recipient_type           TEXT CHECK (recipient_type IN ('judge','participant','guardian','school','admin')),
