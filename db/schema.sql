CREATE OR REPLACE FUNCTION fn_check_timer_role() RETURNS TRIGGER AS $$
DECLARE
    v_role user_role;
BEGIN
    SELECT role INTO v_role FROM users WHERE id = NEW.user_id;
    IF v_role IS DISTINCT FROM 'Timer' THEN
        RAISE EXCEPTION 'User % does not hold the Timer role (has %) -- cannot create a timer_assignment', NEW.user_id, v_role;
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE FUNCTION fn_check_timer_assignment_year() RETURNS TRIGGER AS $$
DECLARE
    v_event_year INT;
BEGIN
    SELECT year_id INTO v_event_year FROM events WHERE id = NEW.event_id;
    IF v_event_year IS DISTINCT FROM NEW.year_id THEN
        RAISE EXCEPTION 'timer_assignments.year_id (%) does not match events.year_id (%) for event %', NEW.year_id, v_event_year, NEW.event_id;
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

