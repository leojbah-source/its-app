-- Rule #1: enforce year_config.max_individual_events and category_cap per participant.
CREATE OR REPLACE FUNCTION fn_check_registration_limits() RETURNS TRIGGER AS $$
DECLARE
    v_max_events     INT;
    v_category_cap   INT;
    v_event_count    INT;
    v_category_count INT;
BEGIN
    IF NEW.participant_id IS NULL THEN
        RETURN NEW; -- team registrations are not subject to per-participant caps
    END IF;

    SELECT max_individual_events, category_cap INTO v_max_events, v_category_cap
    FROM year_config WHERE id = NEW.year_id;

    SELECT count(*) INTO v_event_count
    FROM registrations
    WHERE participant_id = NEW.participant_id
      AND year_id = NEW.year_id
      AND status <> 'withdrawn';

    IF v_event_count >= v_max_events THEN
        RAISE EXCEPTION 'Participant % already registered for the maximum % individual events', NEW.participant_id, v_max_events;
    END IF;

    IF NEW.category_id IS NOT NULL THEN
        SELECT count(*) INTO v_category_count
        FROM registrations
        WHERE participant_id = NEW.participant_id
          AND year_id = NEW.year_id
          AND category_id = NEW.category_id
          AND status <> 'withdrawn';

        IF v_category_count >= v_category_cap THEN
            RAISE EXCEPTION 'Participant % already registered for the maximum % events in this category', NEW.participant_id, v_category_cap;
        END IF;
    END IF;

    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Rule #7 helper: absolute divergence threshold for an event.
CREATE OR REPLACE FUNCTION fn_divergence_abs_threshold(p_year_id INT, p_total_participants INT) RETURNS INT AS $$
DECLARE
    v_pct NUMERIC;
BEGIN
    SELECT divergence_threshold_pct INTO v_pct FROM year_config WHERE id = p_year_id;
    RETURN ROUND(p_total_participants * v_pct / 100.0);
END;
$$ LANGUAGE plpgsql STABLE;
COMMENT ON FUNCTION fn_divergence_abs_threshold(INT, INT) IS 'Rule #7: absolute divergence threshold per event = ROUND(total_participants * divergence_threshold_pct / 100).';

