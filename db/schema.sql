-- Rule: up to 6 criteria per event, max_score values sum to 100.
CREATE OR REPLACE FUNCTION fn_check_event_criteria() RETURNS TRIGGER AS $$
DECLARE
    v_event_id INT := COALESCE(NEW.event_id, OLD.event_id);
    v_count    INT;
    v_sum      NUMERIC;
BEGIN
    SELECT count(*), COALESCE(sum(max_score), 0) INTO v_count, v_sum
    FROM event_criteria WHERE event_id = v_event_id;

    IF v_count > 6 THEN
        RAISE EXCEPTION 'Event % cannot have more than 6 scoring criteria', v_event_id;
    END IF;

    IF v_sum > 100 THEN
        RAISE EXCEPTION 'Event % criteria max_score values sum to % -- must not exceed 100', v_event_id, v_sum;
    END IF;

    RETURN NULL;
END;
$$ LANGUAGE plpgsql;

-- Score entries can never exceed their criterion's max_score.
CREATE OR REPLACE FUNCTION fn_check_score_max() RETURNS TRIGGER AS $$
DECLARE
    v_max NUMERIC;
BEGIN
    SELECT max_score INTO v_max FROM event_criteria WHERE id = NEW.criterion_id;
    IF NEW.score_value > v_max THEN
        RAISE EXCEPTION 'Score % exceeds criterion max_score % (criterion_id=%)', NEW.score_value, v_max, NEW.criterion_id;
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Rule #3: continuous chest numbering across all slots of a time-slot-mode event.
CREATE OR REPLACE FUNCTION fn_recalc_slot_chest_numbers() RETURNS TRIGGER AS $$
DECLARE
    v_event_id INT := COALESCE(NEW.event_id, OLD.event_id);
    v_running  INT := 1;
    r RECORD;
BEGIN
    FOR r IN
        SELECT id, participant_capacity
        FROM event_time_slots
        WHERE event_id = v_event_id
        ORDER BY sort_order, id
    LOOP
        UPDATE event_time_slots
        SET chest_no_start = v_running
        WHERE id = r.id AND chest_no_start IS DISTINCT FROM v_running;
        v_running := v_running + r.participant_capacity;
    END LOOP;
    RETURN NULL;
END;
$$ LANGUAGE plpgsql;

